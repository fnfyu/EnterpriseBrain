# ── Code Execution Tool (E2B Sandbox) ─────────────────────────────────────────

@tool
def execute_python_code(code: str) -> str:
    """
    Execute Python code in a secure cloud sandbox (E2B).
    Use for: data analysis, chart generation, calculations.
    Returns: stdout output and any generated file paths.
    """
    from app.core.config import get_settings
    settings = get_settings()
    try:
        from e2b_code_interpreter import Sandbox
        with Sandbox(api_key=settings.e2b_api_key) as sandbox:
            result = sandbox.run_code(code)
            output = "\n".join(str(l) for l in result.logs.stdout)
            errors = "\n".join(str(l) for l in result.logs.stderr)
            return json.dumps({
                "output": output,
                "errors": errors if errors else None,
            })
    except Exception as e:
        return json.dumps({"error": str(e)})


