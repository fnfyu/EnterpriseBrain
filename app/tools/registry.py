# ── System Operation Tools ────────────────────────────────────────────────────

@tool
def create_ticket(title: str, description: str, priority: str = "medium") -> str:
    """
    Create a support or task ticket in the enterprise ticketing system.
    priority: low | medium | high | urgent
    Returns: ticket ID and status.
    """
    import uuid
    ticket_id = f"TKT-{uuid.uuid4().hex[:8].upper()}"
    logger.info("Ticket created", ticket_id=ticket_id, title=title, priority=priority)
    return json.dumps({
        "ticket_id": ticket_id,
        "title": title,
        "status": "created",
        "priority": priority,
    })


@tool
def call_internal_api(endpoint: str, method: str = "GET", payload: str = "{}") -> str:
    """
    Call an internal enterprise API endpoint.
    endpoint: relative path, e.g. /api/hr/employees
    method: GET | POST | PUT
    payload: JSON string body (for POST/PUT)
    Returns: API response as JSON string.
    """
    import httpx
    from app.core.config import get_settings
    # In production, base_url would be the internal API gateway
    base_url = "http://internal-api:8080"
    try:
        data = json.loads(payload)
        with httpx.Client(timeout=10) as client:
            if method.upper() == "GET":
                resp = client.get(f"{base_url}{endpoint}")
            elif method.upper() == "POST":
                resp = client.post(f"{base_url}{endpoint}", json=data)
            else:
                resp = client.put(f"{base_url}{endpoint}", json=data)
        return resp.text
    except Exception as e:
        return json.dumps({"error": str(e)})


# ── Content Generation Tools ──────────────────────────────────────────────────

@tool
def generate_report(title: str, data: str, report_type: str = "summary") -> str:
    """
    Generate a structured report from provided data.
    data: JSON string of data points.
    report_type: summary | comparison | trend
    Returns: Markdown-formatted report.
    """
    try:
        parsed = json.loads(data)
    except Exception:
        parsed = data

    report = f"# {title}\n\n"
    report += f"**Report Type:** {report_type}\n\n"
    report += "## Data\n\n"
    if isinstance(parsed, list):
        for item in parsed:
            report += f"- {item}\n"
    else:
        report += f"```json\n{json.dumps(parsed, ensure_ascii=False, indent=2)}\n```\n"
    report += "\n_Generated by Enterprise Brain_"
    return report


