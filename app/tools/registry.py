# ── Data Query Tools ──────────────────────────────────────────────────────────

@tool
def query_database(sql: str) -> str:
    """
    Execute a read-only SQL query against the enterprise database.
    Input: a valid SELECT SQL statement.
    Returns: JSON string of query results.
    """
    import sqlalchemy
    from app.core.config import get_settings
    settings = get_settings()
    # Use synchronous connection for tool execution
    sync_url = settings.database_url  # postgresql://...
    engine = sqlalchemy.create_engine(sync_url)
    try:
        with engine.connect() as conn:
            result = conn.execute(sqlalchemy.text(sql))
            rows = [dict(r._mapping) for r in result]
            return json.dumps(rows, ensure_ascii=False, default=str)
    except Exception as e:
        return json.dumps({"error": str(e)})
    finally:
        engine.dispose()


